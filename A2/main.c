#include <stdio.h>
int main() {
    /* Data types
     * int = %d
     * str = %s
     * float= %f
     * char = %c
     */
    int integer=10;
    float floating=10.5;
    char character='A';
    char string[]="Hello World!";

    printf("%s is a string \n",string);
    printf("%d is a int \n",integer);
    printf("%f is a fp \n",floating);
    printf("%c is a char \n",character);
}